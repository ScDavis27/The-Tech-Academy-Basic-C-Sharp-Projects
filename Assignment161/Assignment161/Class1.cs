using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Assignment161
{
    class Class1
    {
        public static int Sum(int x, int y)
        {
            int a = x;
            int b = y;

            int result = a + b;

            return result;
        }


        public static int Product(int x, int y)
        {
            int a = x;
            int b = y;

            int result = a * b;

            return result;
        }


        public static int Divide(int x, int y)
        {
            int a = x;
            int b = y;

            int result = a / b;

            return result;
        }
        
    }
}
